#include "libft.h"

int	ft_isalnum(int c)
{
	if ((!(c >= '0' && c <= '9')))
		if ((!(c >= 'a' && c <= 'z')) && (!(c >= 'A' && c <= 'Z')))
			return (0);
	return (1);
}

